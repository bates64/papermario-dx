#!/usr/bin/env python3
"""User-facing CLI for building and applying modules. See manual/modules.md.

Usage:
  module.py build
  module.py apply <rom> [-o OUTPUT]
"""

import argparse
import os
import subprocess
import sys
from pathlib import Path

try:
    import tomllib
except ModuleNotFoundError:
    import tomli as tomllib

SCRIPT_DIR = Path(__file__).resolve().parent
DX_ROOT = SCRIPT_DIR.parent.parent


def load_module_toml(module_dir):
    toml_path = Path(module_dir) / "module.toml"
    if not toml_path.exists():
        return {}
    with open(toml_path, "rb") as f:
        return tomllib.load(f)


def find_sources(module_dir):
    module_dir = Path(module_dir)
    sources = sorted(module_dir.glob("*.cpp"))
    return [str(p.relative_to(module_dir)) for p in sources]


def generate_build_ninja(module_dir, dx_root, name, sources, version="us"):
    """Generate a build.ninja for a module. All paths are relative to dx_root,
    so the result works both standalone (with -C dx_root) and as a subninja."""
    dx_root = Path(dx_root).resolve()
    module_dir = Path(module_dir).resolve()
    mod = Path(os.path.relpath(module_dir, dx_root))
    build_dir = mod / "build"
    (dx_root / build_dir).mkdir(parents=True, exist_ok=True)

    cross = "mips-linux-gnu-"
    cc = f"{cross}gcc"
    cxx = f"{cross}g++"

    cppflags = (
        f"-Iinclude -Isrc "
        f"-Iver/{version}/include -Iver/{version}/build/include "
        f"-D_FINALROM -DVERSION={version} -DF3DEX_GBI_2 -D_MIPS_SZLONG=32"
    )
    cflags = (
        "-c -G0 -O2 -g1 "
        "-funsigned-char -mgp32 -mfp32 -mabi=32 "
        "-mfix4300 -march=vr4300 -mno-gpopt "
        "-mno-abicalls -fno-pic -fno-exceptions "
        "-fno-stack-protector -fno-zero-initialized-in-bss"
    )
    syms_ld = f"ver/{version}/build/syms.ld"
    module_ld = "tools/build/module.ld"
    module_internal = "tools/build/module_internal.py"

    ninja_path = dx_root / build_dir / "build.ninja"
    with open(ninja_path, "w") as f:
        f.write(f"# Generated by module.py - do not edit\n\n")

        f.write(f"rule mod_cxx\n")
        f.write(f"  command = {cxx} {cflags} {cppflags} -std=c++20 -D_LANGUAGE_C_PLUS_PLUS -include dx/prelude.hpp -MD -MF $out.d $in -o $out\n")
        f.write(f"  depfile = $out.d\n")
        f.write(f"  deps = gcc\n")
        f.write(f"  description = Compiling $desc\n\n")

        f.write(f"rule mod_link\n")
        f.write(f"  command = {cross}ld --emit-relocs -nostdlib -T {module_ld} -T {syms_ld} $in -o $out\n")
        f.write(f"  description = Linking $desc\n\n")

        f.write(f"rule mod_convert\n")
        f.write(f"  command = python3 {module_internal} convert $in $out\n")
        f.write(f"  description = Converting $desc\n\n")

        cxx_pch = "include/dx/prelude.hpp.gch"
        objects = []
        for src in sources:
            src = Path(src)
            obj = str(build_dir / (src.stem + ".o"))
            f.write(f"build {obj}: mod_cxx {mod / src} | {cxx_pch} || generated_code_{version} inc_img_bins_{version}\n")
            f.write(f"  desc = {src.name}\n")
            objects.append(obj)

        elf = str(build_dir / f"{name}.elf")
        module_file = str(build_dir / f"{name}.module")
        f.write(f"\nbuild {elf}: mod_link {' '.join(objects)} | {syms_ld}\n")
        f.write(f"  desc = {name}.elf\n")
        f.write(f"build {module_file}: mod_convert {elf}\n")
        f.write(f"  desc = {name}.elf\n")
        f.write(f"\ndefault {module_file}\n")

    return ninja_path


def cmd_build(args):
    module_dir = Path.cwd()
    config = load_module_toml(module_dir)
    if not (module_dir / "module.toml").exists():
        print("error: no module.toml found in current directory", file=sys.stderr)
        sys.exit(1)

    name = config.get("name", module_dir.name)
    sources = find_sources(module_dir)
    if not sources:
        print("error: no source files found", file=sys.stderr)
        sys.exit(1)

    ninja_path = generate_build_ninja(module_dir, DX_ROOT, name, sources)
    build_dir = module_dir / "build"

    result = subprocess.run(["n2", "-C", str(DX_ROOT), "-f", str(ninja_path)])
    if result.returncode != 0:
        sys.exit(result.returncode)

    module_path = build_dir / f"{name}.module"
    if module_path.exists():
        print(f"\nbuilt {module_path.relative_to(module_dir)}")


def cmd_apply(args):
    module_dir = Path.cwd()
    config = load_module_toml(module_dir)
    if not (module_dir / "module.toml").exists():
        print("error: no module.toml found in current directory", file=sys.stderr)
        sys.exit(1)

    dx = DX_ROOT
    version = "us"
    name = config.get("name", module_dir.name)
    build_dir = module_dir / "build"

    module_path = build_dir / f"{name}.module"
    elf_path = build_dir / f"{name}.elf"
    if not module_path.exists():
        print(f"error: {module_path} not found, run 'module.py build' first", file=sys.stderr)
        sys.exit(1)

    input_rom = Path(args.rom)
    if not input_rom.exists():
        print(f"error: ROM not found: {input_rom}", file=sys.stderr)
        sys.exit(1)

    if args.output:
        output_rom = Path(args.output)
    else:
        output_rom = build_dir / f"{name}.z64"

    syms_path = dx / f"ver/{version}/build/syms.ld"
    if not syms_path.exists():
        print(f"error: syms.ld not found at {syms_path}", file=sys.stderr)
        sys.exit(1)

    MODULE_FLAG_AUTOLOAD = 1 << 0
    flags = 0
    if config.get("autoload", False):
        flags |= MODULE_FLAG_AUTOLOAD

    cmd = [
        sys.executable, str(dx / "tools/build/module_internal.py"),
        "apply", str(input_rom), str(output_rom),
        str(syms_path), name, str(module_path), f"0x{flags:X}",
    ]
    if elf_path.exists():
        cmd.extend(["--elf", str(elf_path)])

    result = subprocess.run(cmd)
    if result.returncode != 0:
        sys.exit(result.returncode)

    print(f"\nwrote {output_rom}")


def main():
    parser = argparse.ArgumentParser(
        prog="module",
        description="Build and apply Paper Mario modules",
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    p_build = subparsers.add_parser("build", help="Build the module in the current directory")
    p_build.set_defaults(func=cmd_build)

    p_apply = subparsers.add_parser("apply", help="Apply the module to a ROM")
    p_apply.add_argument("rom", help="Input ROM file")
    p_apply.add_argument("-o", "--output", help="Output ROM path (default: build/<name>.z64)")
    p_apply.set_defaults(func=cmd_apply)

    args = parser.parse_args()
    args.func(args)


if __name__ == "__main__":
    main()
